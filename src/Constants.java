public class Constants {
    public static void main(String[] args){
        // 상수(contants, final variables)
        // 한 번 값이 할당 => 재할당이 불가능

        final double PI = 3.14; //즉 final 붙이면 상수가 됨.
        // 한 번 값이 할당 => 재할당이 불가능
        // 전체를 대문자, 단어간 구분은 _
        System.out.println(PI);




    } 
}
